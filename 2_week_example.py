# 기본 자료형
a = 1
b = 1
print(a, b)
a = 1.5
b = 3.5
print(a, b)
a = "ddd"
b = 'sss'
print(a, b)
b = True
a = False
print(a, b)

# 제곱승 (**)
print(3 * 3 * 3 * 3 * 3) # 243
print(3 ** 5) # 243

# 형변환
a = float(10)
print(a)    # 10.0
print(int(5.9)) # 5
a = '76.3'  # string
b = float(a) # float
c = str(b) # string
# 타입 확인할때
